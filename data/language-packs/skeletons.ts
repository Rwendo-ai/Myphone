/**
 * Universal lesson skeletons for the Rwendo curriculum.
 *
 * Each entry defines the language-agnostic shape of one lesson position
 * (m01-l01 through m10-l10): which concept slots it teaches, how the 7
 * phases are wired up, and the XP/duration metadata.
 *
 * Concept_keys here are the contract every language pack must fill. When
 * a (target, native) pair is rendered, the engine pulls each concept_key
 * from the target pack (target word) and the native pack (native word)
 * and stitches them into the lesson.
 *
 * Naming conventions (see docs/curriculum/LANGUAGE-PACK-MIGRATION-NOTES.md):
 *   - lower_snake_case
 *   - semantic, language-agnostic ("morning_greeting", not "mangwanani")
 *   - module-namespaced when the slot is grammar-specific to a single
 *     lesson and unlikely to be reused (e.g. "noun_class_mu_va_singular"
 *     vs grammar-translatable concepts like "i_am" / "i_have")
 */

